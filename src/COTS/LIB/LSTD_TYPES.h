#ifndef _LSTD_TYPES_H_
#define _LSTD_TYPES_H_

/**
 * @defgroup standard_library Standard Library
 * @brief This module contains the standard library-related macros, types, and functions
 * @{
 */

/**
 * @defgroup typedefs Standard data types
 * @brief This module contains the standard data types
 * @{
 */

/**
 * @typedef t_bool
 * @brief Type definition for boolean
 */
typedef unsigned char t_bool;

/**
 * @typedef t_u8
 * @brief Type definition for 8-bit unsigned INT
 */
typedef unsigned char t_u8;

/**
 * @typedef t_s8
 * @brief Type definition for 8-bit signed INT
 */
typedef signed char t_s8;

/**
 * @typedef t_c8
 * @brief Type definition for 8-bit char
 */
typedef unsigned char t_c8;

/**
 * @typedef t_u16
 * @brief Type definition for 16-bit unsigned int
 */
typedef unsigned short int t_u16;

/**
 * @typedef t_s16
 * @brief Type definition for 16-bit signed INT
 */
typedef signed short int t_s16;

/**
 * @typedef t_u32
 * @brief Type definition for 32-bit unsigned int
 */
typedef unsigned int t_u32;

/**
 * @typedef t_s32
 * @brief Type definition for 32-bit signed INT
 */
typedef signed int t_s32;

/**
 * @typedef t_u64
 * @brief Type definition for 64-bit unsigned int
 */
typedef unsigned long int t_u64;

/**
 * @typedef t_s64
 * @brief Type definition for 64-bit signed int
 */
typedef signed long int t_s64;

/**
 * @typedef t_fl32
 * @brief Type definition for 32-bit float
 */
typedef float t_fl32;

/**
 * @typedef t_fl64
 * @brief Type definition for 64-bit float
 */
typedef double t_fl64;

/** @} */
/** @} */

#endif /* _LSTD_TYPES_H_ */
